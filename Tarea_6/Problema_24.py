#24.Calcular la suma de una serie numerica.
a = int(input("Ingrese el primer numero de la sucesion:\n"))
an = int(input("Ingrese el ultimo numero de la sucesion:\n"))
n = int(input("Ingrese el numero de terminos de la sucesion:\n"))
def suma_serie(a,an,n):
    return(n/2)*(a+an)
print("La suma de la sucesion fue:", suma_serie(a,an,n))


